// Let & Const Exercise:

// 1. What is the difference between var and let?

// Var is function scoped and Let is block scoped. Variable declared by Let
// cannot be redeclared and must be declared before use. Variables declared with
// Var can be hoisted.

// 2. What is the difference between var and const? 

// You cannot redeclare or reassing using the Const keyword. Const declared Variables
// can be hoisted while Const creates block scoped.

// 3. What is the difference between Let & Const? 

// You cannot redeclare or reassign using the const keyword. You can reassign with Let. 

// 4. What is hoisting?

// Hoisting allows you to use functions and variables before they are declared.